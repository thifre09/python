import turtle

p = turtle.Turtle()
p.speed(10)

pontos = [
    (0,0),
    (100,0),
    (100,100),
    (0,100),
    (0,0),
    (-50,50),
    (50,50),
    (100,0),
    (50,50),
    (50,150),
    (100,100),
    (50,150),
    (-50,150),
    (0,100),
    (-50,150),
    (-50,50),
    (0,0),
    (200,100),
    (300,100),
    (300,200),
    (200,200),
    (200,100),
    (150,150),
    (250,150),
    (300,100),
    (250,150),
    (250,250),
    (300,200),
    (250,250),
    (150,250),
    (200,200),
    (150,250),
    (150,150),
    (-50,50),
    (50,50),
    (250,150),
    (300,100),
    (100,0),
    (100,100),
    (300,200),
    (200,200),
    (0,100),
    (-50,150),
    (150,250),
    (250,250),
    (50,150)
]


def desenho(x=0,y=0,zoom=1):
    p.penup()
    p.goto(x,y)
    p.pendown()
    for ponto in pontos:
        p.goto(zoom*ponto[0]+x,zoom*ponto[1]+y)
    

desenho(-100,-100,1)
turtle.done()

